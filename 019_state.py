a = 10
b = 20
a = b
print(f"a is {a}")
print(f"a is {b}")

#a = 10: This line assigns the value 10 to the variable a.

#b = 20: This line assigns the value 20 to the variable b.

#a = b: This line makes the value of a equal to the value of b. So, a now holds the value 20, because b holds 20.

#print(f"a is {a}"): This is a formatted string (also called an f-string). It prints the value of a. Since we just set a to be 20 in the previous step, it will print: a is 20

#print(f"b is {b}"): This also prints the value of b. Since b is still 20 (as we assigned it earlier), it will print: b is 20
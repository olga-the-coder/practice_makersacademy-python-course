# * Strings: A sequence of characters
# * Lists: A sequence of any item

# * Dictionaries: A collection of keys mapped to values

my_dict = {
    "String": "A sequence of characters",
    "List": "A sequence of any item",
    "Dictionary": "A sequence of key-value pairs",
}

print("A String is:")
print(" " + my_dict["String"])
print("")

print("A List:")
print(" " + my_dict["List"])
print("")

print("A Dictionary:")
print(" " + my_dict["Dictionary"])
print("")
